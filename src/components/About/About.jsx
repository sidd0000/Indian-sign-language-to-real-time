import React, { useEffect, useState } from 'react';
import './About.css';

const AboutUs = () => {
  const [isTyping, setIsTyping] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      const element = document.querySelector('.typing-text');
      const rect = element.getBoundingClientRect();
      const isVisible = rect.top >= 0 && rect.bottom <= window.innerHeight;

      if (isVisible) {
        setIsTyping(true);
      } else {
        setIsTyping(false);
      }
    };

    window.addEventListener('scroll', handleScroll);
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  return (
    <div className="about-us">
      <header className="about-header">
        <h1 >About Us</h1>
        <p>Discover who we are, our mission, and our team.</p>
      </header>
      
      <section className="about-content">
        <div className="about-section">
          <h2 className={`typing-text ${isTyping ? 'typing' : ''}`}>Our Mission</h2>
          <p>
            We strive to develop innovative solutions that make a meaningful difference in the world. Our goal is to leverage technology to drive positive change and enhance people's lives.
          </p>
        </div>

        <div className="about-section">
          <h2 className={`typing-text ${isTyping ? 'typing' : ''}`}>Our Vision</h2>
          <p>
            Our vision is to create a future where technology seamlessly integrates into our daily lives, fostering connectivity, enhancing accessibility, and promoting overall well-being.
          </p>
        </div>

        <div className="about-section">
          <h2 className={`typing-text ${isTyping ? 'typing' : ''}`}>Core Values</h2>
          <ul>
            <li>Innovation: Pushing the boundaries of what's possible</li>
            <li>Integrity: Upholding the highest ethical standards</li>
            <li>Collaboration: Working together to achieve common goals</li>
            <li>Customer Focus: Putting our customers at the heart of everything we do</li>
          </ul>
        </div>

        <div className="about-section">
          <h2 className={`typing-text ${isTyping ? 'typing' : ''}`}>Our Story</h2>
          <p>
            Founded in 2024, our company started with a small team of passionate individuals who shared a vision for a better world. Through hard work and dedication, we have grown into a dynamic organization that continues to push the boundaries of technology.
          </p>
        </div>
      </section>

      {/* Placeholder for future team section */}
      {/* <div className="about-team">
        <h2>Meet the Team</h2>
        <p>Coming Soon...</p>
      </div> */}
    </div>
  );
};

export default AboutUs;
